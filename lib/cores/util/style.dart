import 'package:flutter/material.dart';

TextStyle white(double size) =>
    TextStyle(color: Colors.white, fontWeight: FontWeight.bold, fontSize: size);

TextStyle black(double size) =>
    TextStyle(color: Colors.black, fontWeight: FontWeight.bold, fontSize: size);

TextStyle whiteSingle(double size) =>
    TextStyle(color: Colors.white, fontSize: size);
